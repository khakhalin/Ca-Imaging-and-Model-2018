function caimaging_extra_figures2
% Builds more figures for the paper
% All data is hard-coded in the very end of this file

a = data();

age = a(:,1);
a = a(:,2:end);

s46 = (age==46);
s49 = ~s46;

figure('Color','white');
typeName = {'raw','adj'};
varName = {'Efficiency','Modularity','Clustering','Flow','InIn Assortativity'};
for(iType=1:2)
    for(iVar=1:5)
        col = (iType-1)*5+iVar;         % Column within the data
        h = subplot(2,5,col);
        set(h,'FontSize',8);
        b = concatenan(a(s46,col),a(s49,col));
        compare_columns(b,{'46','49'},'mean');
        title([varName{iVar} ': ' typeName{iType}],'FontSize',8,'FontWeight','Normal');
    end
end

end

